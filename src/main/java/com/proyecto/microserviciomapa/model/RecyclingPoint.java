package com.proyecto.microserviciomapa.model;

import jakarta.persistence.Entity;
import jakarta.persistence.Id;

@Entity
public class RecyclingPoint {

    @Id
    private Long id;
    private int x;
    private int y;
    private int z;
    private int width;
    private int height;
    private String color1;
    private String color2;
    private String color3;

    // No-argument constructor
    public RecyclingPoint() {
    }

    // Constructor with parameters
    public RecyclingPoint(Long id, int x, int y, int z, int width, int height, String color1, String color2, String color3) {
        this.id = id;
        this.x = x;
        this.y = y;
        this.z = z;
        this.width = width;
        this.height = height;
        this.color1 = color1;
        this.color2 = color2;
        this.color3 = color3;
    }

    // Getters and setters
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getZ() {
        return z;
    }

    public void setZ(int z) {
        this.z = z;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public String getColor1() {
        return color1;
    }

    public void setColor1(String color1) {
        this.color1 = color1;
    }

    public String getColor2() {
        return color2;
    }

    public void setColor2(String color2) {
        this.color2 = color2;
    }

    public String getColor3() {
        return color3;
    }

    public void setColor3(String color3) {
        this.color3 = color3;
    }
}